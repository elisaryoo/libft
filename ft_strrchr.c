#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	x;

	x = ft_strlen(s);
	while (x + 1)
	{
		if (s[x] == (char)c)
			return ((char *)&s[x]);
		x--;
	}
	return (0);
}